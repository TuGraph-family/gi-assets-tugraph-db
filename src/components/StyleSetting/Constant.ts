export const operatorMapping = {
  CT: "CONTAINS",
  NC: "CONTAINS",
  EQ: "=",
  NE: "<>",
  GT: ">",
  LT: "<",
  GE: ">=",
  LE: "<="
};

export const getOperatorList = (value: string = '') => {
  const type = value.toLowerCase()
  if (type === "string") {
    return [
      {
        key: "CT",
        value: "⊃",
        text: '包含'
      },
      {
        key: "NC",
        value: "⊅",
        text: '不包含'
      },
      {
        key: "EQ",
        value: "="
      },
      {
        key: "NE",
        value: "≠"
      }
    ];
  }

  if (type === "long" || type === "double" || type === 'int32') {
    return [
      {
        key: "GT",
        value: ">"
      },
      {
        key: "LT",
        value: "<"
      },
      {
        key: "EQ",
        value: "="
      },
      {
        key: "NE",
        value: "≠"
      },
      {
        key: "GE",
        value: "≥"
      },
      {
        key: "LE",
        value: "≤"
      }
    ];
  }

  if (type === "boolean") {
    return [
      {
        key: "EQ",
        value: "="
      },
      {
        key: "NE",
        value: "≠"
      }
    ];
  }
  return [];
};
